public class SolveParabolaEquation {
    public static void main(String[] args) {
        double a = 2.0;
        double b = 3.0;
        double c = 4.1;
        double x = 0.3; 
        double y = a * (x * x) + b * x + c;
        System.out.println("The answer is " + y); //prints the answer to the console
    }
}
